#!/usr/bin/ruby -w

# `!` Boolean Not.
# `~` Bitwise Complement.
# `+` Unary Plus.

puts("[+] Unary Operator code example are following:")
puts(~10) # IT add a 1 in the input we give or present and add the '-' before the output.
puts(~-15) # It decrement a number by '1' but now it doesn't add '-' before output.
puts(1+403)  # Unary plus Operator
puts(!true)  # Boolean Not Operator.
puts(!false) # Boolean Not Operator.

